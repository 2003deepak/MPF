class Review {
  String name;
  String surname;
  String email;
  String phone;
  String address;
  String gender;
  String reviewText;
  int rating;

  Review({
    required this.name,
    required this.surname,
    required this.email,
    required this.phone,
    required this.address,
    required this.gender,
    required this.reviewText,
    required this.rating,
  });
}
